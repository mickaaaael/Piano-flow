// seq(items, hand) : hand = 'R' | 'L' | null
function seq(items, hand=null){
  let t=0, notes=[];
  for(const [m,d] of items){
    if(m!==null) notes.push({midi:m, start:t, dur:d*0.92, hand});
    t+=d;
  }
  return notes;
}
// mélange et trie deux mains + génère les événements pédale
function merge(R, L, pedal=[]){
  return { notes:[...R,...L].sort((a,b)=>a.start-b.start), pedal, hasHands:true };
}
// pédale : liste de {start,end} en secondes
